#!/usr/bin/env node
// Regenerates src/twemoji-map.js from the emoji-datasource package.
//
// Usage:
//   npm run update:twemoji-map

import { writeFileSync } from 'node:fs'
import { join } from 'node:path'
import emojiData from 'emoji-datasource/emoji.json' with { type: 'json' }

const projectRootDirectory = join(import.meta.dirname, '..')
const outputPath = join(projectRootDirectory, 'src', 'twemoji-map.js')

// Fitzpatrick modifiers, in the order gemoji/GitHub's "skin-tone-N" aliases expect them.
const skinToneModifiers = [
  ['1F3FB', 2],
  ['1F3FC', 3],
  ['1F3FD', 4],
  ['1F3FE', 5],
  ['1F3FF', 6],
]

// Mirrors twemoji's own `grabTheRightIcon` (see twemoji.js): a variation selector
// (FE0F) is dropped from the codepoint sequence unless the sequence contains a
// zero-width joiner (200D), in which case it's part of the asset filename.
function toTwemojiCodepoint(unified) {
  const parts = unified.split('-')
  const hasZwj = parts.includes('200D')
  const kept = hasZwj ? parts : parts.filter((part) => part !== 'FE0F')
  return kept.map((part) => Number.parseInt(part, 16).toString(16)).join('-')
}

const map = new Map()
for (const emoji of emojiData) {
  const names = emoji.short_names?.length ? emoji.short_names : [emoji.short_name]
  const codepoint = toTwemojiCodepoint(emoji.unified)
  for (const name of names) {
    map.set(name, codepoint)
  }
  if (!emoji.skin_variations) continue
  for (const [fitzpatrickCode, skinToneNumber] of skinToneModifiers) {
    const variation = emoji.skin_variations[fitzpatrickCode]
    if (!variation) continue
    const skinToneCodepoint = toTwemojiCodepoint(variation.unified)
    for (const name of names) {
      map.set(`${name}-skin-tone-${skinToneNumber}`, skinToneCodepoint)
    }
  }
}

const sortedNames = [...map.keys()].sort((a, b) => a.localeCompare(b))
const entries = sortedNames.map((name) => `  '${name}': '${map.get(name)}',`).join('\n')
const header = '// Generated by tasks/generate-twemoji-map.js from emoji-datasource. Do not edit by hand.'
const output = `${header}\nexport default {\n${entries}\n}\n`

writeFileSync(outputPath, output)
console.log(`Wrote ${sortedNames.length} emoji to src/twemoji-map.js`)